# Варіант 1 через кількість елементів у списку
def minus1(l):
    n=len(l)
    j=0
    for i in range(n):
        if l[i]<0:
            j+=1
    return j

# Вариант 2 безпосередньо через список
def minus2(l):
    j=0
    for i in l:
        if i<0:
            j += 1
    return j

l=[ 20, -33, 16, 21, -5, -66, 74, -3, 27, 87, -4,-3]
print("Вариант 1    Від'ємних чисел у списку : ",minus1(l))
print("Вариант 2    Від'ємних чисел у списку : ",minus2(l))
